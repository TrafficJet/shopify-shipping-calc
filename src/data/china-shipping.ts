export function getCountryBySlug(slug: string): CountryRates | undefined {
  return countryRates.find(c => c.slug === slug);
}

export function getCountriesByRegion(): Record<string, CountryRates[]> {
  return countryRates.reduce((acc, c) => {
    if (!acc[c.region]) acc[c.region] = [];
    acc[c.region].push(c);
    return acc;
  }, {} as Record<string, CountryRates[]>);
}

export function calcChinaRate(countrySlug: string, methodId: string, weightKg: number): number | null {
  const c = getCountryBySlug(countrySlug);
  if (!c) return null;
  const r = c.rates[methodId];
  if (!r) return null;
  return Math.max(r.minCharge, r.perKg * weightKg);
}
